void main() {
  print('while loop:-\n');
  var i = 100;
  while (i < 100) {
    print('hello');
    i++;
  }
  print('do while loop:-\n');
  var j = 200;
  do {
    print('hello do');
    j++;
  } while (j < 200);
}
